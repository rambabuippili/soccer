package com.soccer.model;


public class StandingDetails {

	private String countryInfo;
	private String leagueInfo;
	private String teamInfo;
	private String overallPosition;
	public String getCountryInfo() {
		return countryInfo;
	}
	public void setCountryInfo(String countryInfo) {
		this.countryInfo = countryInfo;
	}
	public String getLeagueInfo() {
		return leagueInfo;
	}
	public void setLeagueInfo(String leagueInfo) {
		this.leagueInfo = leagueInfo;
	}
	public String getTeamInfo() {
		return teamInfo;
	}
	public void setTeamInfo(String teamInfo) {
		this.teamInfo = teamInfo;
	}
	public String getOverallPosition() {
		return overallPosition;
	}
	public void setOverallPosition(String overallPosition) {
		this.overallPosition = overallPosition;
	}

	
}
